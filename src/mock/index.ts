function generateKLineData(symbol: string, days = 120): KLineData[] {
  const basePrices: Record<string, number> = {
    "600519": 1700,
    "000858": 160,
    "601318": 46,
    "300750": 210,
    "000001": 12,
    "600036": 38,
    "002594": 250,
    "601899": 15,
    "000333": 70,
    "600900": 28,
  };

  const basePrice = basePrices[symbol] || 50;
  const data: KLineData[] = [];
  let price = basePrice * 0.9;
  const now = new Date();

  for (let i = days; i >= 0; i--) {
    const date = new Date(now);
    date.setDate(date.getDate() - i);

    const volatility = 0.02;
    const trend = (basePrice - price) / basePrice * 0.01;
    const change = (Math.random() - 0.48) * 2 * volatility * price + trend * price;
    const open = price;
    const close = Math.max(price + change, 0.01);
    const high = Math.max(open, close) * (1 + Math.random() * 0.015);
    const low = Math.min(open, close) * (1 - Math.random() * 0.015);
    const volume = Math.floor(1000000 + Math.random() * 50000000);

    data.push({
      time: date.toISOString().split("T")[0],
      open: Number(open.toFixed(2)),
      close: Number(close.toFixed(2)),
      high: Number(high.toFixed(2)),
      low: Number(low.toFixed(2)),
      volume,
    });

    price = close;
  }

  return enrichKLineData(data);
}

export const getKLineData = (symbol: string): KLineData[] => {
  return generateKLineData(symbol);
};

function generateEquityCurve(initial: number, days: number, annualReturn: number): { date: string; value: number }[] {
  const result: { date: string; value: number }[] = [];
  const dailyReturn = Math.pow(1 + annualReturn / 100, 1 / 252) - 1;
  let value = initial;
  const now = new Date();

  for (let i = days; i >= 0; i--) {
    const date = new Date(now);
    date.setDate(date.getDate() - i);
    value = value * (1 + dailyReturn + (Math.random() - 0.5) * 0.01);
    result.push({
      date: date.toISOString().split("T")[0],
      value: Number(value.toFixed(2)),
    });
  }

  return result;
}

